package Basic.OPP.Task18.Pack2;

public class CaptainBenefit {
    private int id;
    private int benefitCaptain;
    private int allCaptainBenefit;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getBenefitCaptain() {
        return benefitCaptain;
    }

    public void setBenefitCaptain(int benefitCaptain) {
        this.benefitCaptain = benefitCaptain;
    }

    int getAllCaptainBenefit() {
        return allCaptainBenefit;
    }

    void setAllCaptainBenefit(int allCaptainBenefit) {
        this.allCaptainBenefit = allCaptainBenefit;
    }
}
