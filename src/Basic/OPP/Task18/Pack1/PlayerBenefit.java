package Basic.OPP.Task18.Pack1;

public class PlayerBenefit {

    private int id;
    private int benefitPlayer;
    private int allPlayerBenefit;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getBenefitPlayer() {
        return benefitPlayer;
    }

    public void setBenefitPlayer(int benefitPlayer) {
        this.benefitPlayer = benefitPlayer;
    }

    int getAllPlayerBenefit() {
        return allPlayerBenefit;
    }

    void setAllPlayerBenefit(int allPlayerBenefit) {
        this.allPlayerBenefit = allPlayerBenefit;
    }
}
